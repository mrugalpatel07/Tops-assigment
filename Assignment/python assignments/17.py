# Write a Python program to demonstrate string slicing.

str_Temp = "Tops Technologies"

#String[start:end:step]
print(str_Temp[2:])  # [2::] ==> It will print 3rd character
print(str_Temp[0:5]) # [:5] ==>it will only print 5 character 
print(str_Temp[0:21:2])
print(str_Temp[::5])
print(str_Temp[::-1]) # [::-1] ==> it will reverse the strin or [1::2] ==> it will skip the 2 character from string  


